/* USER CODE BEGIN 0 */
void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
  if(ms >= Tx_Delay)
  {
		MCU_Temp = (uint16_t)(357.558 - (float)ADC_Buffer[2] * 0.187364);	//get the mcu temperature from dma register
		  
		POTENTIOMETERS[0] = ADC_Buffer[0] / 340 + 1;
		POTENTIOMETERS[1] = ADC_Buffer[1] / 340 + 1;
		
		Tx_Data[0] = POTENTIOMETERS[0];
		Tx_Data[1] = POTENTIOMETERS[1];
		Tx_Data[2] = BUTTONS[0];
		Tx_Data[3] = BUTTONS[1];
		Tx_Data[4] = BUTTONS[2];
		Tx_Data[5] = BUTTONS[3];
		Tx_Data[6] = BUTTONS[4];
		Tx_Data[7] = (uint8_t)MCU_Temp;
		CanDataTx(TXID);
		if(HAL_CAN_GetTxMailboxesFreeLevel(&hcan) != 0){
			HAL_CAN_AddTxMessage(&hcan, &TxHeader, Tx_Data, &mailbox);
			HAL_IWDG_Refresh(&hiwdg);
		}
		else{
			HAL_CAN_AbortTxRequest(&hcan, 0);
			HAL_CAN_AbortTxRequest(&hcan, 1);
			HAL_CAN_AbortTxRequest(&hcan, 2);
		}
			
		
    ms = 0;
  }
  if(htim->Instance==TIM4)
	{
    ms++;
	}
}
